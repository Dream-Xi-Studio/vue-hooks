/**
 * 时间单位对应的秒数
 */

/** 一年的秒数 */
export const SECONDS_IN_YEAR = 365 * 24 * 60 * 60

/** 一个月的秒数（假设一个月有30天） */
export const SECONDS_IN_MONTH = 30 * 24 * 60 * 60

/** 一天的秒数 */
export const SECONDS_IN_DAY = 24 * 60 * 60

/** 一小时的秒数 */
export const SECONDS_IN_HOUR = 3600

/** 一分钟的秒数 */
export const SECONDS_IN_MINUTE = 60
